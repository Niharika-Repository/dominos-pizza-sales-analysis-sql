--Data Analysis

--1.1 Total unique orders
SELECT
	COUNT(DISTINCT ORDER_ID)
FROM
	ORDERS;

--1.2 Orders by month
WITH
	MONTHLY_ORDERS AS (
		SELECT
			DATE_TRUNC('month', ORDER_DATE) AS MONTH,
			COUNT(ORDER_ID) AS ORDER_COUNT
		FROM
			ORDERS
		GROUP BY
			DATE_TRUNC('month', ORDER_DATE)
	)
SELECT
	MONTH,
	ORDER_COUNT,
	LAG(ORDER_COUNT) OVER (
		ORDER BY
			MONTH
	) AS PREV_MONTH,
	ROUND(
		100.0 * (
			ORDER_COUNT - LAG(ORDER_COUNT) OVER (
				ORDER BY
					MONTH
			)
		) / NULLIF(
			LAG(ORDER_COUNT) OVER (
				ORDER BY
					MONTH
			),
			0
		),
		2
	) MOM_GROWTH_PCT
FROM
	MONTHLY_ORDERS
ORDER BY
	MONTH;

--1.3 Orders by day of week 
SELECT
	TO_CHAR(ORDER_DATE, 'Day') AS WEEKDAY,
	COUNT(DISTINCT ORDER_ID) AS TOTAL_ORDERS
FROM
	ORDERS
GROUP BY
	WEEKDAY
ORDER BY
	TOTAL_ORDERS DESC;

--1.4 Average orders per customer
SELECT
	ROUND(
		COUNT(DISTINCT ORDER_ID) * 1.0 / COUNT(DISTINCT CUSTID),
		2
	) AS AVG_ORDERS_PER_CUSTOMER
FROM
	ORDERS

--1.5 Repeat customers with frequency
SELECT
	CUSTID,
	COUNT(DISTINCT ORDER_ID) AS ORDER_COUNT
FROM
	ORDERS
GROUP BY
	CUSTID
ORDER BY
	ORDER_COUNT DESC;

--1.6 cumulative order trend- Growth trend based on historical data
