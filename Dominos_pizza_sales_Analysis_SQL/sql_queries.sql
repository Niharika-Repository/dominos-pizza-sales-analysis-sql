--2. Total revenue from pizza sales(price * quantity)
SELECT
	SUM(OD.QUANTITY * P.PRICE) AS TOTAL_REVENUE
FROM
	ORDER_DETAILS OD
	JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID;

--3. Hightest priced pizza
SELECT
	PT.NAME,
	P.SIZE,
	CONCAT('$', P.PRICE) AS PRICE
FROM
	PIZZAS P
	JOIN PIZZA_TYPES PT ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
ORDER BY
	P.PRICE DESC;

--4. most common pizza size orderd
SELECT
	P.SIZE,
	COUNT(*) AS TOTAL_ORDERS
FROM
	ORDER_DETAILS OD
	JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID
	JOIN PIZZA_TYPES PT ON PT.PIZZA_TYPE_ID = P.PIZZA_TYPE_ID
GROUP BY
	P.SIZE
ORDER BY
	TOTAL_ORDERS DESC
LIMIT
	1;

--5. Top 5 most ordersd pizza types
SELECT
