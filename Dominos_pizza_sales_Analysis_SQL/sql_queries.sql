--16. Customer Segmentation
WITH
	CUST_SPENT AS (
		SELECT
			C.CUSTID,
			SUM(OD.QUANTITY * P.PRICE) AS TOTAL_SPENT
		FROM
			CUSTOMERS C
			JOIN ORDERS O ON C.CUSTID = O.CUSTID
			JOIN ORDER_DETAILS OD ON O.ORDER_ID = OD.ORDER_ID
			JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID
		GROUP BY
			C.CUSTID
	)
SELECT
	CASE
		WHEN TOTAL_SPENT > 100500 THEN 'High Value'
		ELSE 'Regular'
	END AS SEGMENT,
	COUNT(*) AS CUSTOMER_COUNT
FROM
	CUST_SPENT
GROUP BY
	SEGMENT;

--17. Repeat customer rate
WITH
	CUST_ORDERS AS (
		SELECT
			CUSTID,
			COUNT(DISTINCT ORDER_ID) AS ORDER_COUNT
		FROM
			ORDERS
		GROUP BY
			CUSTID
	)
SELECT
	ROUND(
		100.0 * SUM(
			CASE
				WHEN ORDER_COUNT > 1 THEN 1
				ELSE 0
			END
		) / COUNT(*),
		2
	) AS REPEAT_RATE
FROM
	CUST_ORDERS;



















