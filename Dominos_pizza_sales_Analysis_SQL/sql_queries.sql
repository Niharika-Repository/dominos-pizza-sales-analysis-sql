--7. Order by Hour of the day
SELECT
	TO_CHAR(ORDER_TIME::TIME, 'HH24:00') AS ORDER_HOUR,
	COUNT(*) AS ORDER_COUNT
FROM
	ORDERS
GROUP BY
	ORDER_HOUR
ORDER BY
	ORDER_HOUR;

--8. Average pizza ordered per day
SELECT
	ROUND(AVG(DAILY_TOTAL), 0) AS AVG_PIZZA_PER_DAY
FROM
	(
		SELECT
			O.ORDER_DATE,
			SUM(OD.QUANTITY) AS DAILY_TOTAL
		FROM
			ORDERS O
			JOIN ORDER_DETAILS OD ON O.ORDER_ID = OD.ORDER_ID
		GROUP BY
			ORDER_DATE
	) T;

--9. Top 3 pizzas by revenue
WITH
	PIZZA_REVENUE AS (
		SELECT
			PT.NAME,
			SUM(OD.QUANTITY * P.PRICE) AS REVENUE,
			RANK() OVER (
				ORDER BY
					SUM(OD.QUANTITY * P.PRICE) DESC
			) AS RANK
		FROM
			ORDER_DETAILS OD
			JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID
			JOIN PIZZA_TYPES PT ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
		GROUP BY
			PT.NAME
	)
SELECT
	NAME,
	REVENUE
FROM
	PIZZA_REVENUE
WHERE
	RANK <= 3;

--Advanced Analysis
--10. Revenue contribution per pizza
SELECT
	PT.NAME,
	SUM(OD.QUANTITY * P.PRICE) AS REVENUE,
	CONCAT(
		ROUND(
			100 * SUM(OD.QUANTITY * P.PRICE) / SUM(SUM(OD.QUANTITY * P.PRICE)) OVER (),
			2
		),
		'%'
	) AS PER_CONTRIBUTION
FROM
	ORDER_DETAILS OD
	JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID
	JOIN PIZZA_TYPES PT ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
GROUP BY
	PT.NAME
ORDER BY
	PER_CONTRIBUTION DESC;

--11. Cumulative order revenue over time
SELECT
	ORDER_DATE,
	DAILY_REVENUE,
	SUM(DAILY_REVENUE) OVER (
		ORDER BY
			ORDER_DATE
	) AS CUMULATIVE_REVENUE
FROM
	(
		SELECT
			O.ORDER_DATE,
			SUM(OD.QUANTITY * P.PRICE) AS DAILY_REVENUE
		FROM
			ORDERS O
			JOIN ORDER_DETAILS OD ON O.ORDER_ID = OD.ORDER_ID
			JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID
		GROUP BY
			O.ORDER_DATE
	) T;

--12. Top 3 pizzas by category (revenue based)
WITH
	CAT_RANK AS (
		SELECT
			PT.CATEGORY,
			PT.NAME,
			SUM(OD.QUANTITY * P.PRICE) AS REVENUE,
			RANK() OVER (
				PARTITION BY
					PT.CATEGORY
				ORDER BY
					SUM(OD.QUANTITY * P.PRICE) DESC
			) AS RANK
		FROM
			ORDER_DETAILS OD
			JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID
			JOIN PIZZA_TYPES PT ON P.PIZZA_TYPE_ID = PT.PIZZA_TYPE_ID
		GROUP BY
			PT.CATEGORY,
			PT.NAME
	)
SELECT
	CATEGORY,
	NAME,
	REVENUE
FROM
	CAT_RANK
WHERE
	RANK <= 3;


--Extended business case studies
--13. Top 10 customers by spending
SELECT
	C.CUSTID,
	C.FIRST_NAME || ' ' || C.LAST_NAME AS NAME,
	SUM(OD.QUANTITY * P.PRICE) AS TOTAL_SPENT
FROM
	CUSTOMERS C
	JOIN ORDERS O ON C.CUSTID = O.CUSTID
	JOIN ORDER_DETAILS OD ON O.ORDER_ID = OD.ORDER_ID
	JOIN PIZZAS P ON OD.PIZZA_ID = P.PIZZA_ID
GROUP BY
	C.CUSTID,
	NAME
ORDER BY
	TOTAL_SPENT DESC
LIMIT
	10;

